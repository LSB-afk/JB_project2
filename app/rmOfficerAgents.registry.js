/* RM 역할 하네스 — 전용 agent/skill registry.
   다른 role/affiliate agent registry를 import/alias하지 않는다.
   계층 구조: 오케스트레이터 → 상담 도메인 에이전트(스킬/데이터) → Action/Comms → 준법 가드레일.
   각 에이전트는 개별 산출물(.md)을 만들고, 통합 리포트가 개별 md를 링크한다. */

function rmoAgent(config) {
  return {
    id: config.id,
    agentKey: config.agentKey,
    name: config.name,
    displayName: config.displayName || config.name,
    description: config.description,
    domain: config.domain,
    org: config.org || "JB 금융그룹 RM 하네스",
    responsibilities: config.responsibilities,
    triggerExamples: config.triggerExamples,
    inputSchema: config.inputSchema || ["caseId", "caseType", "situation", "riskLevel", "dueAt"],
    outputSchema: config.outputSchema || ["summary", "evidenceTable", "nextActions", "deliverableFile", "requiresHumanReview"],
    allowedActions: config.allowedActions,
    blockedActions: RMO_FORBIDDEN_OUTPUTS.concat(config.blockedActions || []),
    dbReads: config.dbReads,
    dbWrites: config.dbWrites,
    handoffRules: config.handoffRules,
    guardrails: (config.guardrails || []).concat(["모든 출력은 내부 업무 참고용", "담당 RM/승인권자 검토 필요 표시 유지"]),
    metrics: config.metrics,
    skills: config.skills || [],
    deliverableFile: config.deliverableFile,
    estimatedMinutes: config.estimatedMinutes || 3,
    status: config.status || "active",
    queue: config.queue || 0,
  };
}

